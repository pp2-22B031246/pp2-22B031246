#Upper case 
a = "Hello, World!"
print(a.upper())

#Lower case
a = "Hello, World!"
print(a.lower())

#Remove whitespace 
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"


#Replace string 
a = "Hello, World!"
print(a.replace("H", "J"))

#Split string 
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']
